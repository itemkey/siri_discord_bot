def _extend(seed: tuple[str, ...], target: int, pattern: str) -> tuple[str, ...]:
    values = list(seed)
    index = 1
    while len(values) < target:
        values.append(pattern.format(index=index, base=seed[(index - 1) % len(seed)]))
        index += 1

    return tuple(values)


